let n = 7;
let i = 0;
while(i<n){
    console.log(i);
    i++;
}
// Note: If we do not increment i, then it will be an infinite loop

let m = 5
let j = 0
do{
    console.log(j);
    j++;
}while(j<m)
// Note: In do while loop, the condition is checked after executing the loop body. So, the loop body is executed at least once even if the condition is false   
